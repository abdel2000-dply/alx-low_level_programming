#include "3-calc.h"

/**
 * op_add - adds two num
 * @a: the first num
 * @b: the second num
 * Return: the result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub two num
 * @a: the first num
 * @b: the second num
 * Return: the diffrence of the two numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul two num
 * @a: first num
 * @b: second num
 * Return: the product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div of two num
 * @a: first num
 * @b: first num
 * Return: the division of a by b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - module of two num
 * @a: first num
 * @b: second num
 * Return: the remainder of the division of a by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
